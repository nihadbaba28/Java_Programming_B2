package day16_loops;

public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("------------------");
        int num = 1;
        while (num <= 100){
           System.out.println("Hello world");
           num++;
        }
        int z = 1;
        while (z <= 50){
            System.out.println("Hello camp " +z );
            z ++;
        }
        System.out.println(z);
    }
}

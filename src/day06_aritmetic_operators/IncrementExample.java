package day06_aritmetic_operators;

public class IncrementExample {
    public static void main(String[] args) {

        int i = 5;
        int z = i++; // z = 5 | i = 6

        System.out.println(i);
        System.out.println(z);


        int a = 10;
        int b = a + 1;

        System.out.println(a);
        System.out.println(b);


    }


}
